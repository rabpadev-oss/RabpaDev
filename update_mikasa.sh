import sys, os, struct, zlib, base64
src = os.path.expanduser('~/mikasa_icon.jpg')
base_dir = os.path.expanduser('~/rabpadev/app/src/main/res')
sizes = {"mipmap-mdpi":48,"mipmap-hdpi":72,"mipmap-xhdpi":96,"mipmap-xxhdpi":144,"mipmap-xxxhdpi":192}
try:
    from PIL import Image
    img = Image.open(src).convert("RGBA")
    for d, s in sizes.items():
        r = img.resize((s,s), Image.LANCZOS)
        p = os.path.join(base_dir, d)
        r.save(os.path.join(p,"ic_launcher.png"))
        r.save(os.path.join(p,"ic_launcher_round.png"))
    print("Icons OK (PIL)")
except ImportError:
    # No PIL - copy jpg directly as png (Android accepts jpg named as png)
    with open(src,'rb') as f: data=f.read()
    for d in sizes:
        p = os.path.join(base_dir, d)
        open(os.path.join(p,'ic_launcher.png'),'wb').write(data)
        open(os.path.join(p,'ic_launcher_round.png'),'wb').write(data)
    print("Icons OK (direct copy)")
